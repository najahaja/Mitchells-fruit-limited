# Filter out internal/shipping/coupon items from menu syncs
CLOVER_ITEM_SKIP_PREFIXES = ("print", "gift card", "fb redeem", "waiver")


def _is_clover_internal_item(item: dict) -> bool:
    """
    Excludes free test cards or print service items from the customer catalog.
    """
    price_cents = item.get("price", 0)
    name = item.get("name", "").strip().lower()
    if price_cents == 0:
        return True
    if any(name.startswith(prefix) for prefix in CLOVER_ITEM_SKIP_PREFIXES):
        return True
    return False


async def upsert_menu_item_from_clover(db: AsyncSession, item: dict) -> bool:
    """
    Upserts a single product item returned by Clover POS into our database.
    Resolves the category and creates it if missing.
    """
    from src.utils.db import MenuItem, MenuCategory

    if _is_clover_internal_item(item):
        return False

    clover_id = item.get("id")
    name = item.get("name", "").strip()
    price_cents = item.get("price", 0)
    # Clover prices are in cents, convert back to float dollars (e.g. 350 cents -> 3.50 dollars)
    price = price_cents / 100.0
    hidden = item.get("hidden", False)

    # Determine category name from first category tag element
    cat_elements = item.get("categories", {}).get("elements", [])
    category_name = cat_elements[0].get("name", "Clover Items") if cat_elements else "Clover Items"

    # Find or insert Category
    cat_result = await db.execute(
        select(MenuCategory).where(MenuCategory.name == category_name)
    )
    category = cat_result.scalar_one_or_none()
    if not category:
        category = MenuCategory(name=category_name, is_available=True)
        db.add(category)
        await db.flush()  # flushes category properties to database to yield an autogenerated ID

    # Check for existing menu item by its clover ID
    item_result = await db.execute(
        select(MenuItem).where(MenuItem.clover_item_id == clover_id)
    )
    existing = item_result.scalar_one_or_none()

    now = datetime.now(timezone.utc)
    if existing:
        # Update existing details
        existing.name = name
        existing.price = price
        existing.category_id = category.id
        existing.is_available = not hidden
        existing.updated_at = now
    else:
        # Insert a new MenuItem row
        db.add(MenuItem(
            category_id=category.id,
            name=name,
            price=price,
            is_available=not hidden,
            clover_item_id=clover_id,
            created_at=now,
            updated_at=now,
        ))

    await db.commit()
    return True


async def full_sync_menu_from_clover(db: AsyncSession, clover_items: list[dict]) -> dict:
    """
    Wipes old manually-seeded records and synchronizes with current Clover items.
    
    Why this is used:
    Keeps the DB inventory identical to Clover. Disables missing items and drops empty categories.
    """
    from src.utils.db import MenuItem, MenuCategory
    from sqlalchemy import delete as sa_delete

    # 1. Clear old manually seeded items (items without clover IDs)
    await db.execute(
        sa_delete(MenuItem).where(MenuItem.clover_item_id.is_(None))
    )
    await db.commit()

    # 2. Temporarily set all clover menu items to unavailable
    await db.execute(
        update(MenuItem)
        .where(MenuItem.clover_item_id.is_not(None))
        .values(is_available=False, updated_at=datetime.now(timezone.utc))
    )
    await db.commit()

    # 3. Sync/re-enable items present in Clover
    synced = 0
    skipped = 0
    for item in clover_items:
        result = await upsert_menu_item_from_clover(db, item)
        if result:
            synced += 1
        else:
            skipped += 1

    # 4. Cleanup: Delete categories that have zero active/available items
    all_cats = (await db.execute(select(MenuCategory))).scalars().all()
    for cat in all_cats:
        # FIXED: Changed 'sa_func.count()' to 'func.count()' to resolve the NameError bug
        count = await db.scalar(
            select(func.count()).select_from(MenuItem).where(
                MenuItem.category_id == cat.id,
                MenuItem.is_available == True,
            )
        )
        if not count:
            await db.delete(cat)
    await db.commit()

    return {"synced": synced, "skipped": skipped}


