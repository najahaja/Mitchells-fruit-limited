# 12. NATURAL LANGUAGE PROCESSING (NLP) TRANSCRIPT REGEX PARSER

async def auto_extract_order_items(
    db: AsyncSession,
    order_items_text: str,
    call_summary_text: str = "",
) -> list[dict]:
    """
    Parses conversational text transcripts to extract structured quantities
    matching products in our database.
    
    How this works:
      1. Combines transcript texts and loads all menu items.
      2. For each menu item, does case-insensitive lookups on singular/plural variations.
      3. Uses bidirectional regex search:
         - "qty before name" pattern: matches e.g. "3x Mango Jam" or "2 Mango Jam"
         - "qty after name" pattern: matches e.g. "Mango Jam: 2" or "Mango Jam (1)"
      4. Defaults item quantity count to 1 if no quantity indicator is nearby.
    """
    import re
    from src.utils.db import MenuItem

    # Combine summary and order text fields into a single searchable lowercase block
    combined = f"{order_items_text} {call_summary_text}".strip().lower()
    if not combined:
        return []

    # Get active catalog products
    result = await db.execute(
        select(MenuItem.name, MenuItem.price).where(MenuItem.is_available == True)
    )
    menu_rows = result.all()
    if not menu_rows:
        return []

    extracted: list[dict] = []
    seen_names: set[str] = set()

    for row_name, row_price in menu_rows:
        item_lower = row_name.lower().strip()
        matched = False
        matched_str = item_lower

        # Step A: String matching
        if item_lower in combined:
            matched = True
        # Step B: Plural check - if database name ends with 's', check for singular in text
        elif item_lower.endswith("s"):
            if item_lower.endswith("ies") and len(item_lower) > 3:
                singular = item_lower[:-3] + "y"
                if singular in combined:
                    matched = True
                    matched_str = singular
            elif item_lower.endswith("es") and len(item_lower) > 2:
                singular = item_lower[:-2]
                if singular in combined:
                    matched = True
                    matched_str = singular
            else:
                singular = item_lower[:-1]
                if singular in combined:
                    matched = True
                    matched_str = singular
        # Step C: Singular check - if database name ends with 'y', check for plural in text (e.g. jellies)
        elif item_lower.endswith("y") and len(item_lower) > 1:
            plural = item_lower[:-1] + "ies"
            if plural in combined:
                matched = True
                matched_str = plural

        if not matched:
            continue
        if item_lower in seen_names:
            continue
        seen_names.add(item_lower)

        escaped = re.escape(matched_str)
        qty = 1

        # Step D: Extract Quantity
        # 1. Search for quantity before item name (e.g. "3 Mango Jam" or "2 packs of Lemon Squash")
        m = re.search(r"(\d+)\s*(?:x|packs?|bags?|cases?|units?|bottles?|jars?|cartons?|tins?|boxes?|cans?)?\s*(?:of\s+)?(?:\b(?:the|a|an)\s+)?" + escaped, combined)
        if m:
            qty = int(m.group(1))
        else:
            # 2. Search for quantity after item name (e.g. "Mango Jam: 2" or "Golden Mist Marmalade (3)")
            m = re.search(escaped + r"\s*(?:x|×|:|\()\s*(\d+)", combined)
            if m:
                qty = int(m.group(1))

        # Append structured dictionary item
        extracted.append({
            "item": row_name,
            "quantity": qty,
            "price": row_price,
        })

    return extracted
