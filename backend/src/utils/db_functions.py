# 1. USER AUTHENTICATION QUERIES

async def get_user_by_email(db: AsyncSession, email: str) -> User | None:
    """
    Looks up a User in the database by their unique email address.
    Returns the User object if found, otherwise returns None.
    """
    # select(User) represents "SELECT * FROM users"
    # .where(...) filters rows where User.email matches the search parameter
    result = await db.execute(select(User).where(User.email == email))
    # .scalar_one_or_none() returns the single User record, or None if empty
    return result.scalar_one_or_none()


async def get_user_by_id(db: AsyncSession, user_id: str) -> User | None:
    """
    Looks up a User by their unique primary key ID.
    """
    result = await db.execute(select(User).where(User.id == user_id))
    return result.scalar_one_or_none()


async def create_user(db: AsyncSession, email: str, hashed_password: str, full_name: str) -> User:
    """
    Registers a new admin dashboard User in the database.
    """
    # 1. Instantiate the SQLAlchemy Model
    user = User(email=email, hashed_password=hashed_password, full_name=full_name, is_admin=True)
    # 2. Add to the session transaction unit-of-work
    db.add(user)
    # 3. Write changes to the physical PostgreSQL database
    await db.commit()
    # 4. Refresh properties (loads autogenerated ID, default created_at times, etc.)
    await db.refresh(user)
    return user


async def update_reset_token(db: AsyncSession, user_id: str, token: str, expires: datetime) -> None:
    """
    Saves a hashed password reset token and expiration time for a user.
    """
    # For security, we never store the raw reset token. We hash it with SHA-256 first.
    hashed = hashlib.sha256(token.encode()).hexdigest()
    await db.execute(
        update(User)
        .where(User.id == user_id)
        .values(reset_token=hashed, reset_token_expires=expires)
    )
    await db.commit()


async def clear_reset_token(db: AsyncSession, user_id: str) -> None:
    """
    Clears reset token properties from a user's account after a password reset completes.
    """
    await db.execute(
        update(User).where(User.id == user_id).values(reset_token=None, reset_token_expires=None)
    )
    await db.commit()


async def update_password(db: AsyncSession, user_id: str, hashed_password: str) -> None:
    """
    Updates the hashed password of a user.
    """
    await db.execute(
        update(User).where(User.id == user_id).values(hashed_password=hashed_password)
    )
    await db.commit()


# 2. CALLER & CUSTOMER HISTORY QUERIES

def normalize_phone_number(phone: str) -> str:
    """
    Helper to normalize phone numbers by extracting digits only
    and returning the last 10 characters to bypass formatting mismatches.
    """
    if not phone:
        return ""
    digits = "".join(c for c in phone if c.isdigit())
    return digits[-10:] if len(digits) >= 10 else digits


async def get_caller_by_phone(db: AsyncSession, phone_number: str) -> Caller | None:
    """
    Retrieves Caller contact records using their E.164 phone number.
    Supports robust fallback matching by normalized phone suffix.
    """
    if not phone_number:
        return None
    # 1. First attempt exact match
    result = await db.execute(select(Caller).where(Caller.phone_number == phone_number))
    caller = result.scalar_one_or_none()
    if caller:
        return caller

    # 2. Robust fallback matching by last 10 digits
    norm = normalize_phone_number(phone_number)
    if len(norm) >= 10:
        result = await db.execute(
            select(Caller).where(Caller.phone_number.like(f"%{norm}"))
        )
        return result.scalars().first()
    return None


async def upsert_caller(db: AsyncSession, phone_number: str, customer_name: str | None) -> Caller:
    """
    Upsert pattern (Insert or Update):
    Finds a caller by phone number. If they do not exist, inserts a new one.
    If they exist and lack a saved name, updates their name.
    """
    caller = await get_caller_by_phone(db, phone_number)
    if caller is None:
        # Create a new record if new caller
        caller = Caller(phone_number=phone_number, customer_name=customer_name)
        db.add(caller)
    elif customer_name and not caller.customer_name:
        # Update name if we just discovered it
        caller.customer_name = customer_name
    await db.commit()
    await db.refresh(caller)
    return caller


async def update_caller_last_called(db: AsyncSession, phone_number: str) -> None:
    """
    Updates the timestamp of the last call received from a caller.
    """
    await db.execute(
        update(Caller)
        .where(Caller.phone_number == phone_number)
        .values(last_called_at=datetime.now(timezone.utc))
    )
    await db.commit()


